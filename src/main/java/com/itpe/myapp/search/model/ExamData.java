package com.itpe.myapp.search.model;

public class ExamData {
	private String idx;
	private String turnExam;
	private String classExam;
	private String typeExam;
	private String titleExam;
	private String pkgExam;
	public String getPkgExam() {
		return pkgExam;
	}
	public void setPkgExam(String pkgExam) {
		this.pkgExam = pkgExam;
	}
	public String getIdx() {
		return idx;
	}
	public void setIdx(String idx) {
		this.idx = idx;
	}
	public String getTurnExam() {
		return turnExam;
	}
	public void setTurnExam(String turnExam) {
		this.turnExam = turnExam;
	}
	public String getClassExam() {
		return classExam;
	}
	public void setClassExam(String classExam) {
		this.classExam = classExam;
	}
	public String getTypeExam() {
		return typeExam;
	}
	public void setTypeExam(String typeExam) {
		this.typeExam = typeExam;
	}
	public String getTitleExam() {
		return titleExam;
	}
	public void setTitleExam(String titleExam) {
		this.titleExam = titleExam;
	}
}
